package com.nostalgiaguy.java7features;

public class NumericLiteralWithUnderscore {
	
	public static void main(String[] args) {
		
		int ten=1_0;
		int hundread=1_0_0;
		int kilo=1_00_0;
		
		System.out.println(" Ten \t" + ten);
		System.out.println(" Hundread \t" + hundread);
		System.out.println(" Kilo \t" + kilo);
			
	}

}
